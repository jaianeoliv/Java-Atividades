package br.com.generation.atividades18;

public class Animal {

	private String nome;
	private int idade;
	
	public void emitirSom() {}
	public void subirArvore() {}
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}

	
}
